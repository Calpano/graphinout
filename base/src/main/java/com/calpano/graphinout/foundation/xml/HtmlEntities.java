    /**
     * Map of HTML entities to their Unicode decimal codes, excluding the 5 XML entities. Source:
     * <a href="https://html.spec.whatwg.org/multipage/named-characters.html">HTML Living Standard</a> (HTML Living
     * Standard) This list is comprehensive as of September 2025. Note: Only the first codepoint is used for
     * multi-codepoint entities.
     */
    private static Map<String, int[]> htmlEntities;

    public static boolean contains(String name) {
        initOnce();
        return htmlEntities.containsKey(name);
    }

    private static void define(String entityName, int... codepoints) {
        int[] prev = htmlEntities.put(entityName, codepoints);
        assert prev == null : "Duplicate entity name: " + entityName;
    }

    /**
     * Returns the Unicode decimal code for the given HTML entity name.
     *
     * @param name e.g. 'nbsp'
     * @return format: '{@code &#1234;}'
     */
    public static @Nullable String getDecimalReplacement(String name) {
        initOnce();
        int[] codepoints = htmlEntities.get(name);
        if (codepoints == null)
            return null;
        return Arrays.stream(codepoints).mapToObj(cp -> "&#" + cp + ";").collect(Collectors.joining());
    }

